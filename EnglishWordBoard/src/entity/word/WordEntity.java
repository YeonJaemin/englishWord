package entity.word;

public class WordEntity {

	private int wordNum;
	private String id;
	private String egWord;
	private String koWord;
	private String wdate;
	public WordEntity() {
		super();
	}
	public int getWordNum() {
		return wordNum;
	}
	public void setWordNum(int wordNum) {
		this.wordNum = wordNum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEgWord() {
		return egWord;
	}
	public void setEgWord(String egWord) {
		this.egWord = egWord;
	}
	public String getKoWord() {
		return koWord;
	}
	public void setKoWord(String koWord) {
		this.koWord = koWord;
	}
	public String getWdate() {
		return wdate;
	}
	public void setWdate(String wdate) {
		this.wdate = wdate;
	}
	
	
	
}
